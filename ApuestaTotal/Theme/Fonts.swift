import SwiftUI

struct AppFonts {
    static func regular(size: CGFloat) -> Font {
        return Font.custom("Rubik-Regular", size: size)
    }

    static func bold(size: CGFloat) -> Font {
        return Font.custom("Rubik-Bold", size: size)
    }
}
